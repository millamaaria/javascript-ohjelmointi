/* Muuttujat */
let a = 3
let b = 4

/* Yhteenlasku */
let summa = a + b;
/* Vähennyslasku */
let erotus = a - b;
/* Kertolasku */
let tulo = a * b;
/* Jakolasku */
let osamaara = a / b;
/* Jakojäännös */
let jakojaannos = a % b;

/* Näyttää tulokset */
console.log(summa)
console.log(erotus)
console.log(tulo)
